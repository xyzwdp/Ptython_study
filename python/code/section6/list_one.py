"""
列表定义
"""

name_list = ["a", 3, "lall", True]

print(name_list)
print(type(name_list))
print("------------------------")

# 定义嵌套列表
nest_list = [[1, 2, 4], ['a', 'b', 'c']]

print(nest_list)
print(type(nest_list))
print("------------------")

# 通过索引取出对应位置的数据
print(name_list[0])
print(name_list[-1])
print(nest_list[1])
print(nest_list[-2])
print("---------------")

# 嵌套列表索引
print(nest_list[1][0])
print(nest_list[1][-2])
print("-------------------")

